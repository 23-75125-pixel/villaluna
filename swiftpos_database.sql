  branch_id bigint references public.pos_branches(id) on delete set null,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.pos_sales (
  id text primary key,
  sold_at timestamptz not null default timezone('utc', now()),
  cashier_user_id bigint references public.pos_users(id) on delete set null,
  cashier text not null,
  cashier_name text,
  items jsonb not null default '[]'::jsonb,
  subtotal numeric(12,2) not null default 0.00 check (subtotal >= 0),
  tax numeric(12,2) not null default 0.00 check (tax >= 0),
  discount_type text,
  discount_amt numeric(12,2) not null default 0.00 check (discount_amt >= 0),
  total numeric(12,2) not null default 0.00 check (total >= 0),
  cash numeric(12,2) not null default 0.00 check (cash >= 0),
  change numeric(12,2) not null default 0.00,
