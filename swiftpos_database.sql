create table if not exists public.pos_branches (
  id bigint generated by default as identity primary key,
  name text not null unique,
  branch_number text unique,
  address text,
  contact_number text,
  status public.branch_status not null default 'active',
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.pos_users (
  id bigint generated by default as identity primary key,
  auth_user_id uuid not null unique references auth.users(id) on delete cascade,
  username citext not null unique,
  fullname text not null,
  email citext unique,
  contact_number text,
  role public.app_role not null default 'cashier',
  status public.user_status not null default 'active',
  branch_id bigint references public.pos_branches(id) on delete set null,
  failed_attempts integer not null default 0 check (failed_attempts >= 0),
  locked_until timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.pos_items (
  id bigint generated by default as identity primary key,
  name text not null,
  product_number text unique,
  emoji text default '🏷️',
  price numeric(12,2) not null default 0.00 check (price >= 0),
  stock integer not null default 0 check (stock >= 0),
