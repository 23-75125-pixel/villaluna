create table if not exists public.pos_cashier_daily_summary (
  id bigint generated by default as identity primary key,
  sales_date date not null,
  cashier_user_id bigint not null references public.pos_users(id) on delete cascade,
  cashier text not null,
  cashier_name text,
  branch_id bigint not null references public.pos_branches(id) on delete cascade,
  transactions integer not null default 0 check (transactions >= 0),
  items_sold integer not null default 0 check (items_sold >= 0),
  total_sales numeric(12,2) not null default 0.00 check (total_sales >= 0),
  starting_cash numeric(12,2) check (starting_cash is null or starting_cash >= 0),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint uq_cashier_date_branch unique (sales_date, cashier_user_id, branch_id)
