create or replace function public.current_pos_user_id()
returns bigint
language sql
stable
security definer
set search_path = public
as $$
  select id
  from public.pos_users
  where auth_user_id = auth.uid()
  limit 1
$$;

create or replace function public.current_app_role()
returns public.app_role
language sql
stable
security definer
set search_path = public
as $$
  select role
  from public.pos_users
  where auth_user_id = auth.uid()
  limit 1
$$;

create or replace function public.current_branch_id()
returns bigint
language sql
stable
security definer
set search_path = public
as $$
  select branch_id
  from public.pos_users
  where auth_user_id = auth.uid()
  limit 1
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(public.current_app_role() = 'admin', false)
$$;

create or replace function public.same_branch(target_branch_id bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    public.is_admin()
    or target_branch_id is null
    or target_branch_id = public.current_branch_id()
$$;

