begin;

create extension if not exists pgcrypto;
create extension if not exists citext;

-- ============================================================
--  ENUMS
-- ============================================================
do $$
begin
  if not exists (select 1 from pg_type where typname = 'app_role') then
    create type public.app_role as enum ('admin', 'staff', 'cashier');
  end if;

  if not exists (select 1 from pg_type where typname = 'user_status') then
    create type public.user_status as enum ('active', 'inactive', 'terminated');
  end if;

  if not exists (select 1 from pg_type where typname = 'branch_status') then
    create type public.branch_status as enum ('active', 'inactive');
  end if;

  if not exists (select 1 from pg_type where typname = 'product_category') then
    create type public.product_category as enum (
      'Fertilizers',
      'Lubricants',
      'Hardware',
      'Feeds',
      'Seeds',
      'Chemicals',
      'Others'
    );
  end if;

  if not exists (select 1 from pg_type where typname = 'request_status') then
    create type public.request_status as enum ('pending', 'approved', 'rejected', 'resolved');
  end if;

  if not exists (select 1 from pg_type where typname = 'request_type') then
    create type public.request_type as enum ('manual-request', 'auto-alert');
  end if;
end
$$;

