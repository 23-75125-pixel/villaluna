  branch_id bigint references public.pos_branches(id) on delete set null,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.pos_requests (
  id bigint generated by default as identity primary key,
  item_id bigint references public.pos_items(id) on delete set null,
  item_name text not null,
  stock integer not null default 0 check (stock >= 0),
  type public.request_type not null default 'manual-request',
  requested_by_user_id bigint references public.pos_users(id) on delete set null,
  requested_by text not null,
  requested_by_name text,
